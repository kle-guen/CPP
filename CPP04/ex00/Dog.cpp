#include "Dog.hpp"


/**************************************************************************************/
/*                                  canonical form                                    */
/**************************************************************************************/


Dog::Dog(void) : Animal()
{
    setType("Dog");
	std::cout << "A Dog is born" << std::endl;
}

Dog::Dog(const Dog &src) : Animal(src)
{
	*this = src;
}

Dog	&Dog::operator=(const Dog &c)
{
	setType(c.getType());
	return (*this);
}

Dog::~Dog(void)
{
	std::cout << "A Dog died" << std::endl;
}



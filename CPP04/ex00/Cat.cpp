#include "Cat.hpp"


/**************************************************************************************/
/*                                  canonical form                                    */
/**************************************************************************************/


Cat::Cat(void) : Animal()
{
    setType("Cat");
	std::cout << "A Cat is born" << std::endl;
}

Cat::Cat(const Cat &src) : Animal(src)
{
	*this = src;
}

Cat	&Cat::operator=(const Cat &c)
{
	setType(c.getType());
	return (*this);
}

Cat::~Cat(void)
{
	std::cout << "A Cat died" << std::endl;
}

